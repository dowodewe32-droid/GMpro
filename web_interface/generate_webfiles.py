def compress_file(filepath, outname):
    """Compress a file to gzip and return C array format"""
    with open(filepath, 'rb') as f:
        data = f.read()
    
    compressed = gzip.compress(data, 9)
    
    # Generate C array format
    hex_values = ', '.join(f'0x{b:02x}' for b in compressed)
    
    # Split into lines of 16 values each for readability
    lines = []
    for i in range(0, len(hex_values), 16 * 6):  # approximate
        lines.append(hex_values[i:i+16*6])
    
    return compressed, '\n'.join(lines)

